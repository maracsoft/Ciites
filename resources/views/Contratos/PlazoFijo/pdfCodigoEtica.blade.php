    <b>
        D.	CONDUCTA INTERNA
    </b>
    <div style="margin-left: {{$margen}}; text-align:justify" >
        -	Todos somos responsables de generar un ambiente de trabajo agradable, motivado por el diálogo y el respeto mutuo. Se espera que los colaboradores compartan sus conocimientos y estimulen el perfeccionamiento profesional de sus colegas, buscando la excelencia de manera colectiva. 
        <br>-	Las relaciones interpersonales deben establecerse sobre la confianza y el trato justo, cada persona es responsable por sus acciones y debe considerar las consecuencias que estas generan sobre las demás. 
        <br>-	Está fuera de lugar el uso de formas de hostilidad, coacción, asedio moral, o uso del poder para obtener ventajas o imponer autoridad, pues afectan la cohesión organizacional y la autoestima personal,  
        <br>-	{{$contrato->getTrabajadore()}}  debe contribuir a fomentar un ambiente de trabajo positivo, motivador, de equidad y promover el trabajo en equipo, para así evitar la desmotivación, la discriminación y el fraude
        <br>-	No a la discriminación: Todo acto o declaración que pone en desventaja a los individuos de origen, nacionalidad, color, género, religión diferentes u otros criterios pertinentes tales como la apariencia, discapacidad, edad, orientación sexual, etc. o que los humilla o ridiculiza se considera como acto de discriminación y por consiguiente {{$contrato->getTrabajadore()}}  se compromete a no tolerarlos ni hacia el interior de la Institución, ni hacia terceros que tengan vinculación.
        <br>-	No al acoso/La intimidación: {{$contrato->getTrabajadore()}}  entiende y rechaza el acoso/la intimidación entendida como la exclusión y humillación sistemática de alguien por una o mas personas. Es decir una conducta hostil que sucede repetidamente y durante un periodo de tiempo significativo. Los casos típicos de acoso son actos que atentan contra la reputación o relaciones sociales de una persona, obstaculizan la posibilidad de comunicación, menosprecian la calidad de trabajo o hasta dañan la salud.  
        <br>-	No se considera como acoso una discusión acalorada o llamada de atención, que pudiera pasar en cualquier circunstancia de trabajo, con tal de que el incidente o intercambio de pareceres se maneje de forma abierta y que  pueda servir para cambios positivos.
        <br>-	No al acoso sexual: {{$contrato->getTrabajadore()}}  entiende que hay una línea muy fina pero clara entre ser amigable, servicial y el acoso sexual: cualquier clase amigable de contacto estrecho con alguien se basa en el acuerdo mutuo entre dos personas. Sin embargo, el acoso sexual ocurre cuando una persona intencionalmente no mantiene la distancia en circunstancias inoportunas en las que no ha sido invitado a ello. Los individuos experimentan ‘el no mantener la distancia’ de maneras diferentes, por lo tanto, no hay un límite general de aceptación. Dicha conducta implica la degradación de una persona en base a su sexo y es ofensivo, humillante, y en muchos casos hasta implica una amenaza a las víctimas. Así que, {{$contrato->getTrabajadore()}}  declara no tolerarlo ni aceptarlo. 
        <br>-	Queja falsa: {{$contrato->getTrabajadore()}}  reconoce que la acusación falsa y la información engañosa deberán ser sujeto de una acción disciplinaria en el grado apropiado.
