<div class="anexo_subtitulo">
  RECOMENDACIONES DE SEGURIDAD Y SALUD EN EL TRABAJO
</div>

<br>
<br>

<div>

  Como trabajador de El
  <b>
    CENTRO PARA LA INVESTIGACIÓN, INNOVACIÓN Y DESARROLLO TERRITORIAL SOSTENIBLE DE LA LIBERTAD - CIITES
  </b>
  y en cumplimiento al Artículo 35 de la Ley N°29783: Ley de Seguridad y Salud en el Trabajo y D.S. 005-2012- TR,
  <span class="subrayado">
    me comprometo a cumplir con las siguientes recomendaciones:
  </span>
  <br>
  En la Planificación de mis actividades o tareas, debo:
</div>


<ul class="quitar_margenes">
  <li>
    Vigilar que mi Ambiente de Trabajo sea Seguro, y no presente peligros latentes (peligros no controlados).
  </li>
  <li>
    Dar mi negativa a desarrollar a un Trabajo Inseguro.
  </li>
  <li>
    Observar las medidas de Seguridad al trabajar.
  </li>
  <li>
    Estar muy familiarizado con los Procedimientos de Seguridad que corresponden a mis tareas.
  </li>
  <li>
    Saber usar los Controles específicos de Seguridad
  </li>
  <li>
    Solicitar ayuda cuando me asignen tareas que no puedo hacer solo sin peligro. Existen tareas que NO pueden ser hechas sin peligro por una sola persona.
  </li>
  <li>
    No trabajar horas extras realizando tareas peligrosas que no son parte de mi trabajo normal.
  </li>
  <li>
    No asignar tareas o trabajos a personas que no estén calificadas.
  </li>
  <li>
    No asignar tareas o trabajos a personas que no se encuentre en el estado de Salud adecuado por dicha labor.
  </li>
  <li>
    Planificar mi Trabajo y las medidas de Seguridad: Tomaré el tiempo para planificar mi trabajo a solas y con otras personas.
  </li>
  <li>
    La planificación con otras personas es especialmente útil. Me permite coordinar mi trabajo y aprovechar el conocimiento de los demás sobre la identificación y control de Peligros.
  </li>
</ul>
<br>

<div class="subrayado">
  La planificación de la Seguridad es una parte importante de cualquier tarea. Reconocer, evaluar y controlar los Peligros que requiere mi esfuerzo.
</div>

<ul class="quitar_margenes">
  <li>
    Cumplir con las normas de seguridad dadas para los estándares de trabajo desarrollados.
  </li>
</ul>

<div>
  En la ejecución de mis actividades o tareas, debo:
</div>

<ul class="quitar_margenes">
  <li>
    Desarrollar Prácticas de Trabajo Seguras que ayuden a controlar el riesgo a sufrir lesiones o perder la vida ante Peligros presentes en el lugar de trabajo.
  </li>
  <li>
    No fomentar el Trabajo Inseguro del personal a cargo o del personal dCIITES. El cntro NO acepta que ningún trabajador realice actos Inseguros que ningún personal promueva la ejecución de Trabajos Inseguros. Usar mi buen criterio y sentido común.
  </li>

</ul>

<div>
  En el cierre o término de mis actividades o tareas, debo:
</div>
<ul class="quitar_margenes">
  <li>
    Culminar las prácticas de Trabajo de manera Segura, asegurarme de que tengo el tiempo suficiente para finalizar la tarea asignada sin correr riesgos.
    <span>
      Si estoy apurado, es más probable que trate de ahorrar tiempo con atajos que pueden ser mortales, que no ayudan a controlar el riesgo de sufrir lesiones y que puedan generar la perdida de mi vida o la de terceros ante peligros presentes.
    </span>
  </li>
</ul>
<br>


<div>
  Del trajabador
</div>


